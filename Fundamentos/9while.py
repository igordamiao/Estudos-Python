# O laço while é uma estrutura de repetição que executa um bloco de código enquanto uma condição booleana (que pode ser verdadeira ou falsa) permanece verdadeira
print("Exemplo de while")
contador = 0
while contador <= 10:
    print("Contagem:", contador)
    # contador = contador + 1
    contador += 1
# Não recomendado   if contador == 410:
